function setLanguage(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      // Se for um dos textos de servi√ßo e contiver '\n', renderiza como lista
      if ((key === 'service_01_body' || key === 'service_02_body' || key === 'service_03_body') && translations[lang][key].includes('\n')) {
        const items = translations[lang][key].split('\n').map(item => `<li>${item}</li>`).join('');
        el.innerHTML = `<ul class='list-disc list-inside space-y-1'>${items}</ul>`;
      } else if (key === 'about_body' && translations[lang][key].includes('\n')) {
        el.innerHTML = translations[lang][key].split('\n').join('<br><br>');
      } else if (key === 'about_title' || key === 'services_title' || key === 'where_title' || key === 'customers_title' || key === 'contact_title') {
        const text = translations[lang][key];
        const lines = text.split('\n');
        const html = lines.map(line => `<span>${line}</span>`).join('<span class="text-custom-color">');
        el.innerHTML = html;
      } else {
        el.textContent = translations[lang][key];
      }
    }
  });
  setPlaceholders(lang); // Adiciona tradu√ß√£o de placeholders
  localStorage.setItem('site-lang', lang);

  // Reinicializa anima√ß√£o typewriter
  const heroTitle = document.querySelector('h2.hero-title[data-i18n="hero_title"]');
  if (heroTitle) {
    let fullText = heroTitle.textContent || heroTitle.innerText;
    heroTitle.textContent = "";
    let i = 0;
    function typeWriter() {
      if (i < fullText.length) {
        heroTitle.innerHTML = heroTitle.textContent + fullText.charAt(i) + '<span class="cursor"></span>';
        i++;
        setTimeout(typeWriter, 80);
      } else {
        heroTitle.innerHTML = fullText + '<span class="cursor"></span>';
      }
    }
    typeWriter();
  }
}

function setPlaceholders(lang) {
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[lang] && translations[lang][key]) {
      el.setAttribute('placeholder', translations[lang][key]);
    }
  });
}

